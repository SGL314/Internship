function addContent(object) {
  if(object.images){ 
  addImage(object.images[0])
  } else{

  const imgWrapper = document.querySelector(".img")
    imgWrapper.innerHTML = ''
  } 
  const p = document.querySelector(".content p");  
  p.innerHTML = "<br>"+object.content; // para não ficar colado com a imagem
}

function addImage(imagePath){ // called by addContent(arg)
  const img = document.createElement('img')
  img.src = `imgs/${imagePath}`
  img.id = "imageBigZoom"
  if (imagePath != "semImagem.png"){
    img.className = "imageWithZoom";

    img.addEventListener("mouseenter", () => { // GPT: código não roda
      const prop = screen.height/img.height; // GPT: como pegar o tamanho da tela
      
      img.style = "position: relative;height: 100vh;width: 100%;object-fit: cover;"; // GPT: para naõ distorcer imagem "object-fit: cover;"
    });

    img.addEventListener("mouseleave", () => {
      img.style = "width: auto; height: auto; transition: transform 0.5s ease;";
    });
  }

  

  const imgWrapper = document.querySelector(".img")
  imgWrapper.innerHTML = ''
  imgWrapper.append(img)
  }

