  function addContent(object) {
   if(object.images){ 
    addImage(object.images[0])
   } else{
  
    const imgWrapper = document.querySelector(".img")
      imgWrapper.innerHTML = ''
   } 
    const p = document.querySelector(".content p");  
    p.innerHTML = object.content;
  }
  
  function addImage(imagePath){
   const img = document.createElement('img')
   img.src = `imgs/${imagePath}`
   const imgWrapper = document.querySelector(".img")
   imgWrapper.innerHTML = ''
   imgWrapper.append(img)
  }
  
  function cleanActive() {
    const activeElement = document.querySelector(".active");
    if (activeElement) activeElement.classList.remove("active");
  }
  
  const indice = document.querySelector(".indice ul");
  
  verbetes.forEach((item) => {
    const li = document.createElement("li");
    li.id = item.id;
    li.innerHTML = item.title;
    li.addEventListener("click", (e) => {
      cleanActive();
      e.target.classList.toggle("active");
      addContent(item);
    });
  
    indice.appendChild(li);
  });
  