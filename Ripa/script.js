function addContent(object) {
  if(object.images){ 
    for (var i = 0; i < object.images.length;i++){
      addImage(object.images[i],i)
      setTimeout(()=>{},1000);  
      const p = document.querySelector(".p"+(i+1));  
      p.innerHTML = "<br>"+object.content+"<br><br>"; // para não ficar colado com a imagem
    }
  }else{
    const imgWrapper = document.querySelector(".img")
    imgWrapper.innerHTML = ''
  } 
  
}

function addImage(imagePath,id){ // called by addContent(arg)
  const img = document.createElement('img')
  img.src = "imgs/"+imagePath
  img.className = "imageBigZoom"
  img.id = ""+(id+1)
  if (imagePath != "semImagem.png"){
    img.className = "imageWithZoom";

    img.addEventListener("mouseenter", () => { // GPT: código não roda
      const prop = screen.height/img.height; // GPT: como pegar o tamanho da tela
      
      img.style = "position: relative;height: 100vh;width: 100%;object-fit: cover;"; // GPT: para naõ distorcer imagem "object-fit: cover;"
    });

    img.addEventListener("mouseleave", () => {
      img.style = "width: auto; height: auto; transition: transform 0.5s ease;";
    });
  }

  setTimeout(()=>{},50);  
  
  var section = document.querySelector(".content") 
  
  const div = document.createElement("div");
  div.className = "img"+(id+1)  
  div.append(img)
  section.append(div)
  
  const p = document.createElement("p")
  p.className = "p"+(id+1)
  section.append(p)

  
  
  
}

function cleanActive() {
  document.querySelector(".content").remove()

  const activeElement = document.querySelector(".active");
  if (activeElement) activeElement.classList.remove("active");

  var section = document.createElement("section")
  section.className = "content"
  const container = document.querySelector(".container");
  
  container.append(section)  
}

